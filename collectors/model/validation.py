    @property
    def time(this):
        return this._time

    @time.setter
    def time(this, value):
        this._time = value

    @property
    def symbol(this):
        return this._symbol

    @symbol.setter
    def symbol(this, value):
        this._symbol = value

    @property
    def market(this):
        return this._market

    @market.setter
    def market(this, value):
        this._market = value

    @property
    def actual_time(this):
        return this._actual_time

    @actual_time.setter
    def actual_time(this, value):
        this._actual_time = value

    @property
    def period(this):
        return this._period

    @period.setter
    def period(this, value):
        this._period = value

    @property
    def msg(this):
        return this._msg

